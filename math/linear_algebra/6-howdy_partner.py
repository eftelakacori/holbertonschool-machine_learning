#!/usr/bin/env python3
"""defines function 2D matrices containing ints/floats"""


def cat_arrays(arr1, arr2):
    """ Concatenates two arrays.
    Args:
        arr1 (list): The first array (list of ints/floats).
        arr2 (list): The second array (list of ints/floats).
    Returns:
        list: A new list that is the concatenation of arr1 and arr2."""
    return arr1 + arr2
